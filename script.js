let score = 0;
let total = 0;

function loadWeek() {
  const week = document.getElementById("weekSelector").value;
  const container = document.getElementById("quizContainer");

  container.innerHTML = "";
  score = 0;

  if (!data[week]) {
    container.innerHTML = "<p>No data available</p>";
    return;
  }

  total = data[week].length;
  updateProgress();

  data[week].forEach((item, index) => {
    const div = document.createElement("div");
    div.classList.add("question");

    let optionsHTML = "";

    item.options.forEach((opt) => {
      optionsHTML += `
                <div class="option" onclick="selectOption(this, ${index}, \`${item.answer}\`)">
                    ${opt}
                </div>
            `;
    });

    div.innerHTML = `
            <p><b>Q${index + 1}:</b> ${item.q}</p>
            <div class="options">${optionsHTML}</div>
            <p class="result" id="result${index}"></p>
        `;

    container.appendChild(div);
  });
}

function selectOption(element, index, correct) {
  const parent = element.parentElement;

  // prevent re-answer
  if (parent.classList.contains("answered")) return;

  parent.classList.add("answered");

  const options = parent.querySelectorAll(".option");

  let selectedText = element.innerText.trim();
  let correctText = correct.trim();

  options.forEach((opt) => {
    if (opt.innerText.trim() === correctText) {
      opt.classList.add("correct");
    }
  });

  if (selectedText === correctText) {
    element.classList.add("correct");
    score++;
  } else {
    element.classList.add("wrong");
  }

  document.getElementById(`result${index}`).innerHTML =
    selectedText === correctText ? "✅ Correct" : `❌ Wrong (Ans: ${correct})`;

  updateProgress();
}

function updateProgress() {
  let progressText = document.getElementById("progressText");
  let progressFill = document.getElementById("progressFill");

  if (progressText) {
    progressText.innerText = `${score} / ${total}`;
  }

  if (progressFill) {
    progressFill.style.width = `${(score / total) * 100}%`;
  }
}
